                        <article>
                            <div class="title"><a href="./noticia3.php">La promoción de hábitos saludables como causa</a></div>
                            <div class="icons">
                            	
                                <span class="fecha">5 de Marzo de 2014</span>
                                
                        </div>
                            <div class="imagen"><img alt="img1" src="./css/img/noticia3.jpg" /></div>
                            
                 <div class="text"> Escritoras, enfermeras, empresarias, bioquímicas, comunicadoras sociales, madres, esposas. Mujeres que en su trabajo cotidiano combinan mágicamente sus múltiples roles y apuestan a la difusión de hábitos saludables para tener una mejor calidad de vida.

<br>Algunas de ellas desarrollan sus habilidades comunicacionales en espacios como los centros de salud, donde el deseo de brindar una atención primaria de calidad se enfrenta con un entorno adverso, en el que el derecho a la salud está atravesado por la exclusión social.

<br>Sandra Reyes, de 49 años es licenciada en enfermería y trabaja en la zona rural El Gateado en el norte de la ciudad de Córdoba, en Rancagua Kilómetro 7 ½, antes de llegar a la Colonia Tirolesa. Ella trabaja hace nueve años en el Centro de Salud Nº 94 de barrio Liceo III Sección y la zona rural El Gateado está a unos diez kilómetros de este centro, pasando por Villa Retiro. En este sitio no hay acceso al servicio de agua potable para la mayoría de sus residentes, unas 900 personas que ocupan asentamientos.

<br>“El agua de riego de una acequia es consumida por muchos niños de la zona y tenemos muchos casos de piodermitis (infecciones en la piel), falta de higiene personal, pediculosis, problemas odontológicos como caries y mujeres en edad fértil con falta de piezas dentarias”, describe.

<br>La zona no solo está alejada geográficamente, sino que no tienen acceso a línea de transporte, lo que dificulta la llegada al centro de salud. “Urge abrir una posta sanitaria en el Gateado, porque no entran ambulancias, es camino de tierra y cuando se descomponen niños o embarazadas se complican los traslados y los móviles de la policía terminan llevando a la gente al hospital”, advierte.

<br>Durante siete años, Reyes se trasladó con Marisa Roano, la trabajadora social que la acompaña en su auto particular a la zona de El Gateado. “Logramos implementar el programa de inmunizaciones de vacunación para niños y adultos y la tramitación de certificados de buena salud”, recuerda. Ella explica que, antes, la población de El Gateado acudía al centro de salud, pero no conseguía turno por la nutrida demanda de atención. “Me enorgullece que, a pesar de las limitaciones, logramos fortalecer la identidad barrial a través del trabajo participativo con las mujeres, padres y niños. Hay mujeres muy humildes, postergadas, mucho embarazo adolescente y falta de controles ginecológicos. Hay mujeres que nunca se hicieron un papanicolau”, enumera, alarmada.

<br>También aclara que se necesita una médica ginecóloga y un pediatra, pues hay 110 niños y 95 chicos en secundario. Los padres envían mensajes de texto a su teléfono celular para preguntarle si está presente el doctor y ella coordina los turnos. “La primera vez que fui, me partió el alma ver las carencias, la desigualdad, la injusticia social y la falta de equidad. Nosotros escuchamos a la gente, conocemos sus caras, cómo viven y esto va más allá del acto técnico de poner una vacuna. Hacer salud sin humanidad no sirve, el vínculo es clave”, sostiene.

<br>Este enfoque es compartido por Nora Vilches, que es bioquímica y especialista en salud pública. Ella trabajó durante 22 años en el ex hospital Martínez Estévez y en el Centro de salud Nº 58 de barrio Maldonado y se jubiló a fines del año pasado. “Fortalecer el vínculo con las comunidades es clave para educar en atención primaria”, dice convencida. Este centro atiende a vecinos de los barrios Maldonado, Muller, Villa Inés, Campo de La Rivera y Bajada San José.

<br>“Cuando empecé en 1992, me sorprendió la cantidad de niños con parasitosis, el embarazo adolescente y la contaminación ambiental que había con los permanentes volcamientos de basura ?en la zona”, recuerda la bioquímica.

<br>Sin embargo, en la zona el embarazo adolescente no era percibido por la comunidad como un problema social. “Si una chica de 17 años no había quedado embarazada, llamaba la atención de sus pares. Ellas tenían ansias de tener un hijo y era parte de su proyecto de vida. Decían ‘quiero tener algo que sea mío’, ?recuerda.

<br>Vilches menciona como una fortaleza el trabajo en equipo con los trabajadores sociales, médicos, psicólogos y nutricionistas y reconoce la brecha entre la teoría y la práctica. “Uno viene de una formación académica y desde un ejercicio de la práctica vinculada con la actividad de laboratorio. Pero cuando salís al terreno y empezás a tratar con la gente, todo cambia”, admite. Y cuenta: “Una vez, fuimos a la casa de una señora y le dejamos el conservante para la muestra de un análisis parasitológico. Se lo dimos por escrito y la señora nos miró muy seria. Mi compañera le preguntó si sabía leer y ella respondió que no. Nunca hay que dar nada por sentado”. Vilches considera que la mejor estrategia en prevención es crear vínculos con la comunidad en la que uno trabaja. “Nunca hay que creerse que uno es más que ellos, hay que hablar en términos simples, escuchar y escuchar. “El desafío es promover cada día los derechos humanos, el derecho a la salud, a la educación sexual y reproductiva para seguir difundiendo 
los valores de la promoción de la salud”, concluye la bioquímica.

<br>Más de 100 años después, el mismo espíritu de sacrificio que llevó a pensar en un escenario igualitario entre los géneros impulsa a estas y a otras mujeres a trabajar para un objetivo similar, pero transversal a todos los segmentos sociales, edades y formación. La incorporación de hábitos más saludables para evitar las enfermedades crónicas no transmisibles y males de la época, como la depresión, demanda un abordaje desde todos los perfiles que enriquecen a la sociedad.</div>
 <h2>Perfil</h2>
                  <div class="text">“Hoy, el propósito sigue siendo comprometer a cada compañera/o a entender nuestro rol como servidores públicos y, a través de nuestros actos, proponer lo mejor de nosotros para nuestros semejantes, o sea, ejercer la atención profesional humanizada”, resume Marta Scorpo, licenciada en Enfermería, quien trabajó durante 33 años en atención primaria de la salud, integró el equipo del centro sanitario de Villa El Libertador y hoy participa en el zonal Nº 5 de la Dirección de Atención Primaria de la Salud (DAPs). </div>
                  
                   <h2>Conmemorar con nuevas banderas</h2>
                   <div class="text">“El peor error es encerrarse puertas adentro, ahí perdés, porque no llegas a la gente ni sabés si estás haciendo lo correcto”, afirma convencida Nora Vilches, licenciada en Bioquímica y especialista en salud pública, quien durante 22 años y hasta que se jubiló en diciembre, trabajó en el ex hospital Martínez Estévez y en el Centro de salud Nº 58 de barrio Maldonado. 

Marta Scorpo es licenciada en enfermería y trabajó durante 33 años en atención primaria de la salud. Estuvo en el equipo del centro de salud de Villa Libertador y hoy integra el equipo zonal nº 5 de la Dirección de Atención Primaria de la Salud (DAPs) de la Municipalidad de Córdoba, que agrupa a 18 centros de salud.

Por su experiencia, considera que las actitudes y valores son parte de una mística profesional. “Son cualidades personales que favorecen la identificación individual y con los demás a través de un trato directo y humanitario. La idea es dar lo mejor de uno mismo, con el objetivo de recuperar o enfrentar las enfermedades. Hacer que los demás se sientan bien con acciones o cuidados que se brindan también es estar bien con uno mismo”, reconoce. <br>

Narrar experiencias y expectativas después de más de 30 años no resulta cosa sencilla –asevera–, “mucho menos cuando en mi vida no debía descuidar mi rol de esposa y madre”.

Cuenta que el concepto de la enfermería ha evolucionado a través del tiempo experimentando cambios en los cuales han influido diversas causas. “Esta transición va desde el concepto de la enfermería basado en el cuidado enfocado en la persona misma, hasta la enfermera con adquisición de conocimientos teóricos orientados a una atención más integra”.<br>

Asegura que estas actitudes y valores son partes de una mística profesional, cualidades personales que favorecen la identificación individual, que se identifique con los demás en el trato directo, que refleje siempre lo mejor de sí mismo en todo lo que se realiza con el único objetivo de ayudar al mantenimiento, recuperación de salud o enfrentamiento a la enfermedad, hacer que los demás se sientan bien con acciones o cuidados que se brindan y por ende estar bien con uno mismo. “Con el transcurso del tiempo la enfermería obtuvo con mucho esfuerzo y tenacidad un nivel de profesionalización sustancial en diferentes ámbitos de la salud”.<br>

En sus talleres de escritura que funcionan en el El Club de la Cicatriz, la escritora Cristina Loza trabaja hace nueve años en la resiliencia a partir de la lectura y de la producción de textos. El disparador puede ser un cuento, fragmento de una novela, un artículo de una revista o una nota periodística, es un espacio de encuentro para compartir experiencias donde cada uno da su opinión. <br>

Caminar y correr para ellas es tan bueno como escribir una frase o una poesía. Son las integrantes de Voces Vitales.<br>

“Viví la primera caminata en el primer programa de mentores en Nueva York en 2008 donde participaron 35 mujeres líderes emergentes de todo el mundo del ámbito económico, político, social y de derechos humanos”, relata Lorena Piazze, cofundadora de Voces Vitales Argentina y fundadora de Voces Vitales Delegación Córdoba. <br>

Piazze y María Celia Allende ilustran cómo la socialización, entrelazada con la actividad física, son estrategias aplicables a la promoción de la salud.  </div>
                   
                            
                      
