                            <div class="imagen"><img alt="img1" src="./css/img/blog3.jpg" /></div>
                            
                            <div class="text"> Vidrios Piazze (Vidpia) lanzó ayer una red de fidelización que espera reunir a 500 clientes, entre negocios y profesionales, en el plazo de un año.

La fábrica cordobesa de vidrios y cristales procesados para la construcción realizó este anuncio en el marco de la primera edición de Vidpia Educa, un evento 
que reunió a clientes de todo el país, en el cual presentó novedades de sus productos y las marcas que comercializa y capacitó en las nuevas tendencias en materia de obras y herramientas de gestión.</div>
                            <div class="text">El evento estuvo encabezado por el presidente de la firma, Osvaldo Piazze; su vicepresidente, Hugo Piazze, y su directora Comercial y de Marketing, Lorena Piazze.<br>

La empresa cuenta en el país con aproximadamente mil clientes, de los cuales intentará fidelizar a la mitad en el primer año, según adelantó Lorena Piazze.</div>
                            
                            <div class="text">La Red Vidpia no tendrá costos y apunta a vidrieros, carpinterías, estudios de arquitectura y empresas constructoras, sin que estas pierdan su identidad.</div>
                           <div class="text">A ellos les ofrecerá servicios y beneficios con instituciones con las cuales Vidpia tiene acuerdos o convenios, como el Banco Galicia (que ofrecerá operatoria bancaria y financiación en condiciones más ventajosas), Seguros La Segunda y Corcam (que brindará leasing para sus camiones).

<br>La red ofrecerá una tarjeta de fidelización y, dejaron trascender sus organizadores, podría incluir en un futuro próximo también una tarjeta de crédito.

<br>Además, el programa incluye comunicación de todos los productos; formación en marketing , finanzas, administración y atención al cliente, como también sus productos y complementos.</div>
  <h2>Inversión</h2>
                           <div class="text"> La empresa fue creada en 1945 por Jorge Piazze, quien llegó a convertirse en el principal proveedor de los vidrios redondos que tenían las antiguas cajas de lata para galletas. En la actualidad, está dirigida por la segunda y tercera generación. Tiene una planta de 13 mil metros cuadrados en Circunvalación (entre Camino a San Carlos y Camino a San Antonio) y cinco centros de distribución en esta ciudad y en Corrientes, Mendoza, San Juan y Villa Mercedes (San Luis).<br>

Cuenta además con 280 empleados y procesa entre 1.200 y 1.500 toneladas de vidrio por mes, utilizando más del 70 por ciento de su capacidad productiva. Puede procesar 45 mil metros cuadrados de vidrio templado y 54 mil metros cuadrados de doble vidriado hermético.

<br>La firma ha colocado sus productos en Chile, Paraguay y Miami. Ha participado de obras como el Teatro Solís de Uruguay. En Buenos Aires, aportó vidrios para edificios como la última filial de los bancos Ciudad de Buenos Aires y Galicia y ahora se sumará a un proyecto del desarrollista Eduardo Constantini.

<br>Según destacó Hugo Piazze, las grandes obras exigen ahora utilizar vidrios para ahorro energético, que reducen la entrada de frío-calor a más de la mitad y permiten un mayor uso de luz natural.

<br>Para no perder terreno en este mercado, en estos días están importando un nuevo horno PRO-E “único en el país” de templado horizontal para procesar vidrios de baja emisividad, lo que permite producir vidrios de simple, doble y triple capa de plata.

<br>La inversión es financiada con un crédito del Bicentenario y se instrumentará desde el año próximo.</div>
 <h2>Inflación, un problema</h2>
                           <div class="text">Para Hugo Piazze, el principal problema del sector es la inflación, que redujo al mínimo las exportaciones y achicó la rentabilidad. <br>Este año, los salarios del sector aumentaron 28 a 30 por ciento, cuando los precios de sus productos subieron 16%.</div>
                           
