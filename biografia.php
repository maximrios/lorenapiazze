                            <h2>  YO, MUJER, EMPRENDEDORA, EMPRESARIA...</h2>
                            <div class="text">

Siempre creí que todo tiene un sentido. Que cuando uno desea algo intensamente, existe un segundo remoto en el cual cabe la posibilidad de que eso suceda. También estoy segura de que siempre ese segundo tiene que ir acompañado de muchos segundos más, que no son remotos, que han sido planificados, meditados y que requieren de mucho trabajo. Pero ese segundo es donde se define la oportunidad y uno puede tomarla o dejarla pasar. Porque la vida está llena de oportunidades latentes, que están ahí a la espera de que alguien las descubra y se apodere de ellas. Y es junstamente en ese espacio donde, como mujeres emprendedoras, tenemos una gran fortaleza y es el hecho de tener una visión distinta de las cosas, de ver y enfrentar los problemas con otra naturaleza y abordar el vaso como medio lleno, aunque tenga sólo unas pocas gotas. 
.</div>
 <h2>LA EMPRESA </h2>
                            <div class="text">
Desde muy chica supe que quería trabajar, así que mientras cursaba el último año del secundario me incorporé en la empresa familiar. Había algo especial que me movilizaba, cierta adrenalina que me impusaba a crear, en un ámbito en el cual mis ideas podían encontrar una razón de ser. Pero al mismo tiempo sabia que me faltaba capacitación y aunque hubiera disfrutado estudiar cinco carreras universitarias, sabía que por algún lado había que empezar. Y así elegí administración de empresas, luego contador público, hice especialización en comercio exterior, una diplomatura en recursos humanos y hasta en en gestión de pymes. 
Mi carrera en la empresa empezó sacando fotocopia, luego haciendo de cadete, hasta ocupar el rol de directora hace aproximadamente dos años. La verdad es que aprendí a disfrutar de cada puesto de trabajo por el que transité, lo que me permitió ir conociendo la empresa desde abajo. Crecí con la empresa y aprendí mucho de ella y fundametalmente de mi padre, quien ha sido mi gran mentor. Fue de él de quien aprendí lo importante que es tener una visión y contar con alquien que te tome de la mano cuando necesitás un impulso.
La empresa es mi vocación, siempre cuento la anécdota que cuando llego a un lugar no veo un edificio o una casa, veo una envolvente de vidrios acompañada de estructura. Me apasiona el negocio que inició mi abuelo apenas llegado de Italia, negocio que convirtieron en empresa mi tío Osvaldo y mi padre Hugo. En la actualidad, la empresa es hoy timoneada por ellos y por nosotros, la tercera generación: mis primos Angela y Osvaldo (h), mi hermana Romina y yo, apoyados por la labor de un sin número de compañeros de trabajo. 
</div>
 <h2>MIS MIEDOS </h2>
 <div class="text">
Mi principal miedo es alentar a alguien en algún proyecto que pueda ser incorrecto. Porque se que tengo la particularidad de movilizar y estimular a la gente, ya que estoy convencida de que cuando alguien se propone y busca algo lo consigue. Y lo que más me preocuparía sería empujar, por mi iniciativa, a alguien a cometer un error. No quiero que esa virtud pueda volverse negativa y por lo tanto trabajo mucho en moderar mis impulsos de generar y sumar ideas a gente con proyectos.

</div>
  <h2> VOCES VITALES ARGENTINA</h2>
  <div class="text">
En el 2008 la Embajada de Estados Unidos me invitó a postularme en un programa de líderes emergentes, que me iba a hacer vivir una experiencia única: un networking junto a más de 200 mujeres, de las cuales 35 de ellas eran como yo, pero de distintos países del mundo.
De allí puedo destacar 3 cosas. La primera es "la leccion de liderazgo". Mi mentora decía que "una no es una autentica líder hasta que no es totalmente autentica”, es decir ser una misma.  Ella me empujo a mirarme internamente, a analizar mi propia autenticidad como líder. Eso me hizo pensar que difícil es ser exactamente tal cual somos, particularmente cuando chocamos contra las percepciones y expectativas de los otros. Por ahi se espera que hagamos o digamos tal o cual cosa, que sigamos ciertos formatos, ciertos mandamientos que no estan escritos en ningun lado pero parece que todos se los saben de memoria: cuándo te vas a casar ? cuándo vas a tener hijos? te parece irte a estudiar por tanto tiempo y dejar a tu marido solo? La clave es defender nuestra propia impronta, nuestra marca personal. 
La segunda cosa importante la rescato de una nota que hace tiempo le hicieron a una de mis mentoras y me sentí tan identificada. Ella dijo, "Creo que gran parte de mi crédito es gracias a mis padres, mi papá siempre me decía: chica, a ti te han dado dones. Úsalos” Y lo que él le quería decir con eso era que no sólo use su talento para tu propio beneficio, sino que haga algo más, algo significativo para el resto. Mis padres siempre me dijeron, sos distinta defendelo, disfrutalo y aprovechalo. Cuando volví decidí que todo esto no solo debía ser una buena experiencia para mi, sino que debía ver la forma de que tambien le llegue al resto. En ese momento supe que tenía que ser un agente de cambio y que no volveria a ser la misma. Que debía contagiar a otras personas con todo lo que viví

Y asi generé la Delegacion de Voces Vitales Argentina en Cordoba en el 2010, 
</div>
 <h2> ESPOSA Y MADRE...</h2>
   <div class="text">
Javier apareció en mi vida en una cita horrible a ciegas. Almorzamos y nos despedimos. Después el chat y la distancia hicieron lo suyo. Mientras el viajaba 
por Colombia, Ecuador y Chile, yo lo hacía por Sudáfrica,USA y Uruguay, los dos ocupados, pero solos. La vida nos unió y hoy llevamos 8 años de casados y no me imagino mi vida al lado de otro hombre. No somos perfectos, ni somos una combinación exacta, somos muy opuestos pero juntos creamos una gran familia, tres niños hermosos: los mellizos Bruno y Agostina de y Katia, de 9 meses. 
Ser madre es lo mas satisfactorio que me paso en la vida,  no me imagine que seria tan grandioso, no sabia como resolvería la ecuación de madre + trabajo, pero tenía claro que no quería sacrificar tiempo para una u otra cosa. Me gusta trabajar y disfruto ser madre, y con un poco de planificación y apoyo incondicional de Javier logré combinar ambas pasiones 
</div>
 <h2>HIJA Y HERMANA... </h2>
<div class="text">
Tengo la suerte de tener una madre, Martha, increíble, que siempre creyó en mi y busco la forma de guiarme sin ahogarme, de cuidarme sin apretarme. Una mujer única con un corazón enorme, siempre dispuesta a todo. Mi hermana, Romina, 2 años menor, tan centrada que por ahí parece la mayor, me conoce con apenas mirarme y es capaz de bajarme a la realidad cuando estoy volando. Es un gran apoyo. 
</div>
 <h2>ARGENTINA </h2>
<div class="text">
Desde al visión de empresaria entendí que para que nuestra empresa crezca, permanezca y se mantenga, su comunidad también debe hacerlo y que debemos ser parte de las instituciones que hacen que la comunidad funcione. Así empece a formar parte de distintas organizacionesinstituciones, con distintos fines todas pero con la comunidad como eje central. Si queremos que algo cambie debemos ser parte del cambio, no podemos ser meros espectadores
Es fácil sentarse a esperar que las cosas cambien y si las decisiones tomadas no son las correctas es mas fácil aun criticarlas, ser parte de ambas es lo laborioso pero creo que es lo correcto. 
No es fácil, no siempre hay tiempo, muchas veces tenemos que concensuar y a veces es difícil llegar a un consenso, pero cuando vemos que el fin es el mismo entendemos que estamos construyendo algo más que nuestra propia realidad. 
</div>
