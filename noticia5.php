• Laura Busnelli, Presidente de BUPLASA (Pyme industria del plástico), mentoreada por: Andrea Jung –Avon en 2009.<br>
• Vanina Ubino, Gerente Comercial de Global News, mentoreada por Helene D. Gayle de CARE USA en 2009<br>
• Daniela Martin, Directora de Gestión de AmCham Argentina, mentoreada por Donna Milroad- Deustche Bank Americas en 2010.<br>
