Los factores que primaron a la hora de otorgar el premio fueron el trabajo en conjunto que han demostrado las ocho participantes al regreso de sus experiencias y la labor que el Capítulo Argentino de Voces Vitales logró en sus primeros tres 3 años de vida.<br>
