Esta distinción se otorga anualmente a las mujeres líderes internacionales que hayan participado del FORTUNE/ US STATE GLOBAL WOMEN LEADERSHIP MENTORING PROGRAM por el Departamento de Estado de los Estados Unidos junto a la revista Fortune. En el caso de María Gabriela Hoch, ella formó parte del programa realizado en 2007 y a su regreso inició la co-fundación del capítulo local de Voces Vitales que este año celebra la tercera edición de sus actividades.<br>
